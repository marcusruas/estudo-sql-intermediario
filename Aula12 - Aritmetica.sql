-- FUNCAO MAX - TRAZ O VALOR MAXIMO DE UMA COLUNA

SELECT MAX(FEVEREIRO) AS "MAIOR LUCRO EM FEVEREIRO"
FROM VENDEDOR;

-- FUNCAO MIN - TRAZ O VALOR MINIMO DE UMA COLUNA

SELECT MIN(FEVEREIRO) AS "MENOR LUCRO EM FEVEREIRO"
FROM VENDEDOR;

-- FUNCAO AVG - TRAZ A MEDIA DE UMA COLUNA

SELECT AVG(FEVEREIRO) AS "MEDIA DE LUCRO EM FEVEREIRO"
FROM VENDEDOR;

-- VARIAS FUNCOES

SELECT MAX(JANEIRO) AS MAIOR_JAN,
	   MIN(JANEIRO) AS MENOR_JAN,
	   AVG(JANEIRO) AS MEDIA_JAN
FROM VENDEDOR;

--TRUNCATE

SELECT MAX(JANEIRO) AS MAIOR_JAN,
	   MIN(JANEIRO) AS MENOR_JAN,
	   TRUNCATE(AVG(JANEIRO),2) AS MEDIA_JAN
FROM VENDEDOR;

-- FUNCAO SUM()

SELECT SUM(JANEIRO) AS TOTAL_JAN,
SUM(FEVEREIRO) AS TOTAL_FEV,
SUM(MARCO) AS TOTAL_MAR
FROM VENDEDOR;

-- VENDA POR SEXO
SELECT SEXO, SUM(MARCO) AS TOTAL_MAR
FROM VENDEDOR
GROUP BY SEXO;