IDVEN INT PRIMARY KEY AUTO_INCREMENT,
NOME VARCHAR(30) NOT NULL,
SEXO ENUM('M','F') NOT NULL,
JANEIRO FLOAT(10,2),
FEVEREIRO FLOAT(10,2),
MARCO FLOAT(10,2)
